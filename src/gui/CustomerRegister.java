    public void loadCustomers() {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `customer` INNER JOIN `city` ON `customer`.`city_id`=`city`.`id` ORDER BY `customer`.`id` ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString(("customer.id")));
                v.add(rs.getString(("customer.name")));
                v.add(rs.getString(("customer.mobile")));
                v.add(rs.getString(("city.name")));

                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }
    
    public void loadCities() {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `city`");

            Vector v = new Vector();
            v.add("Select");

            while (rs.next()) {
                v.add(rs.getString("name"));

            }

            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(dcm);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

