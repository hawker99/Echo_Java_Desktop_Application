    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        int selectedRow = jTable1.getSelectedRow();
        
        if(selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Please Select a Supplier", "Warning", JOptionPane.WARNING_MESSAGE);
        
        }else{
            
            String id = jTable1.getValueAt(selectedRow, 0).toString();
            String currentStatus = jTable1.getValueAt(selectedRow, 5).toString();
            
            int status;
            
            if(currentStatus.equals("1")){
                status = 0;
            }else{
                status = 1;
            }
            
            MySQL.iud("UPDATE `supplier` SET `status`="+status+" WHERE `id`="+Integer.parseInt(id)+"");
            
            loadSuppliers();
            
            JOptionPane.showMessageDialog(this, "Supplier Status Updated Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        if (evt.getClickCount() == 2) {
            int r = jTable1.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please Select a Company", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                if (grn != null) {

                    String sid = jTable1.getValueAt(r, 0).toString();
                    String sname = jTable1.getValueAt(r, 1).toString();
                    String cnumber = jTable1.getValueAt(r, 2).toString();
                    String company = jTable1.getValueAt(r, 3).toString();
                    

                    grn.jLabel2.setText(sid);
                    grn.jLabel4.setText(sname);
                    grn.jLabel6.setText(cnumber);
                    grn.jLabel8.setText(company);

                    grn.jButton1.setText("Update Supplier");

                    this.dispose();

                }

            }
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

