    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String name = jTextField1.getText();
        String cnumber = jTextField2.getText();
        String cid = jLabel4.getText();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Supplier Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("07[01245678][0-9]{7}").matcher(cnumber).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid mobile number", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (cid.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select a Company", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs = MySQL.search("SELECT * FROM `supplier` WHERE `mobile`='" + cnumber + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "This Supplier Already Exists", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    MySQL.iud("INSERT INTO `supplier`(`name`,`mobile`,`company_id`) VALUES('" + name + "','" + cnumber + "','" + cid + "')");

                    jTextField1.setText("");
                    jTextField2.setText("");
                    jLabel4.setText("None");
                    jLabel5.setText("None");
                    jLabel7.setText("None");
                    jLabel9.setText("None");
                    jButton3.setText("Select Company");

                    jTextField1.grabFocus();

                    loadSuppliers();

                    JOptionPane.showMessageDialog(this, "New Supplier Account Created Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTextField2KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyTyped
        
        String mobile = jTextField2.getText();
        String text = mobile + evt.getKeyChar();

        if (mobile.length() == 10) {
            evt.consume();
        } else {
            if (!Pattern.compile("[0-9]+").matcher(text).matches()) {
                evt.consume();
            }
        }
        
    }//GEN-LAST:event_jTextField2KeyTyped

