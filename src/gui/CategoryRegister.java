    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String cname = jTextField1.getText();
        
        if(cname.isEmpty()){
            
            JOptionPane.showMessageDialog(this, "Please Enter a Category Name", "Warning", JOptionPane.WARNING_MESSAGE);
            
        }else{
            
            try {
                
                ResultSet rs = MySQL.search("SELECT * FROM `category` WHERE `name`='"+cname+"'");
                
                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "This Category Already Exists", "Warning", JOptionPane.WARNING_MESSAGE);
                }else{
                    MySQL.iud("INSERT INTO `category`(`name`) VAlUES('"+cname+"')");
                    JOptionPane.showMessageDialog(this, "New Category Added Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                    
                    loadCategories();
                    jTextField1.setText("");
                    jTextField1.grabFocus();
                    this.pr.loadCategories();
                    
                }
                
            } catch (Exception e) {
                e.printStackTrace();
            }
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

