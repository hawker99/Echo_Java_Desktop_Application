    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        int selectedRow = jTable1.getSelectedRow();
        
        if(selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Please Select a User", "Warning", JOptionPane.WARNING_MESSAGE);
        
        }else{
            
            String id = jTable1.getValueAt(selectedRow, 0).toString();
            String currentStatus = jTable1.getValueAt(selectedRow, 7).toString();
            
            int status;
            
            if(currentStatus.equals("1")){
                status = 0;
            }else{
                status = 1;
            }
            
            MySQL.iud("UPDATE `user` SET `status`="+status+" WHERE `id`="+Integer.parseInt(id)+"");
            
            loadUsers();
            
            JOptionPane.showMessageDialog(this, "User Status Updated Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

