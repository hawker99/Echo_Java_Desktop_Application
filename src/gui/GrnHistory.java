    public void loadGrn() {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `grn` INNER JOIN `grn_payment` ON `grn`.`id`=`grn_payment`.`grn_id` INNER JOIN `supplier` ON `supplier`.`id`=`grn`.`supplier_id` INNER JOIN `user` ON `user`.`id`=`grn`.`user_id` ORDER BY `grn`.`id` ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString(("grn.id")));
                v.add(rs.getString(("supplier.id")));
                v.add(rs.getString(("supplier.name")));
                v.add(rs.getString(("grn.date_time")));
                v.add(rs.getString(("user.id")));
                v.add(rs.getString(("user.fname")));
                v.add(rs.getString(("grn_payment.payment")));

                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

