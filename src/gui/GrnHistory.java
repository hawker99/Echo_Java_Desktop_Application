    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (jTable1.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Please Search GRN", "Warning", JOptionPane.WARNING_MESSAGE);

        }else{
            
            try {
                
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String dNow = sdf.format(new Date());
                
//                InputStream pathStream = GrnHistory.class.getResourceAsStream("../report//Echo-grnreport.jasper");
                String path = "src//report//Echo-grnreport.jasper";
//                JasperReport jr = JasperCompileManager.compileReport(pathStream);

                HashMap parameters = new HashMap();
                parameters.put("Parameter1", dNow);
                

                TableModel tm = jTable1.getModel();
                JRTableModelDataSource datasource = new JRTableModelDataSource(tm);

                JasperPrint jp = JasperFillManager.fillReport(path, parameters, datasource);
                JasperViewer.viewReport(jp, false);
                
            } catch (Exception e) {
                e.printStackTrace();
            }
            
        }

    }//GEN-LAST:event_jButton2ActionPerformed

