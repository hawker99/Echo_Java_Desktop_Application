    public void loadDetails(){
        int uid = Login.userId;
        
        try {
            
            ResultSet rs = MySQL.search("SELECT * FROM `user` INNER JOIN `user_type` ON `user`.`user_type_id`=`user_type`.`id` WHERE `user`.`id`='"+uid+"'");
            rs.next();
            String id = rs.getString("user.id").toString();
            String fname = rs.getString("user.fname").toString();
            String lname = rs.getString("user.lname").toString();
            String mobile = rs.getString("user.mobile").toString();
            String username = rs.getString("user.username").toString();
            String password = rs.getString("user.password").toString();
            String type = rs.getString("user_type.name").toString();
            
            jLabel11.setText(id);
            jLabel3.setText(fname+" "+lname);
            jLabel5.setText(mobile);
            jLabel7.setText(username);
            jLabel9.setText(password);
            jLabel13.setText(type);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

