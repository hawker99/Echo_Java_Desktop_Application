    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String mobile = jTextField2.getText();
        
        if(!Pattern.compile("07[01245678][0-9]{7}").matcher(mobile).matches()){
            JOptionPane.showMessageDialog(this, "Invalid Mobile Number", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.grabFocus();
        }else{
            
            MySQL.iud("UPDATE `user` SET `mobile`='"+mobile+"' WHERE `id`='"+Login.userId+"'");
            jTextField2.setText("");
            JOptionPane.showMessageDialog(this, "Mobile updated successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            loadDetails();
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTextField2KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyTyped
        String mobile = jTextField2.getText();
        String text = mobile + evt.getKeyChar();

        if (mobile.length() == 10) {
            evt.consume();
        } else {
            if (!Pattern.compile("[0-9]+").matcher(text).matches()) {
                evt.consume();
            }
        }
    }//GEN-LAST:event_jTextField2KeyTyped

