    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        if (evt.getClickCount() == 2) {
            int r = jTable1.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please Select a Product", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                
                if (grn != null) {
                    String pid = jTable1.getValueAt(r, 0).toString();
                    String category = jTable1.getValueAt(r, 1).toString();
                    String brand = jTable1.getValueAt(r, 2).toString();
                    String name = jTable1.getValueAt(r, 3).toString();

                    grn.jLabel16.setText(pid);
                    grn.jLabel15.setText(category);
                    grn.jLabel14.setText(brand);
                    grn.jLabel13.setText(name);
                    
                    grn.jButton2.setText("Update Product");
                    grn.jTextField1.grabFocus();
                    this.dispose();
                }
            }
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

